public class Feedbackclass {
    String key;

    String name;


    public Feedbackclass(String key, String name) {
        this.key = key;
        this.name = name;


    }

    public Feedbackclass() {
    }



    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }




}